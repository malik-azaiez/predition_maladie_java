    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        try { model1.setRowCount(0);
            stm2=conn2.getConnection().createStatement();
            ResultSet Rs=stm2.executeQuery("SELECT id, nom_maladie, gravite_maladie, description_maladie FROM maladie WHERE reference = '"+txtre.getText()+"'");
            while(Rs.next()){
                model1.addRow(new Object[]{Rs.getString("id"),Rs.getString("nom_maladie"),Rs.getString("gravite_maladie"),Rs.getString("description_maladie")});

        }
        }catch(Exception e){System.err.println(e);}

        tble1.setModel(model1);
       
        try { model2.setRowCount(0);
            stm2=conn2.getConnection().createStatement();
            ResultSet Rs=stm2.executeQuery("SELECT id, traitement_maladie FROM traitement WHERE reference = '"+txtre.getText()+"'");
            while(Rs.next()){
                model2.addRow(new Object[]{Rs.getString("id"),Rs.getString("traitement_maladie")});

        }
        }catch(Exception e){System.err.println(e);}

        tble.setModel(model2);
    }//GEN-LAST:event_jButton5ActionPerformed

